// 산술 연산자 (Arithmetic operators)
// +
// -
// * 
// /
// %
// ** 지수(거듭제곱)

console.log(5 + 2);
console.log(5 * 2);
console.log(5 - 2);
console.log(5 % 2);
console.log(5 ** 2);
console.log(5 ** 7); // es7부터 추가
console.log(Math.pow(5,2));

